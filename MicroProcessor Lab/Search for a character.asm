DATA SEGMENT
	MSG1 DB "HELLO WORLD$"
	MSG2 DB 10,13,"CHARACTER IS PRESENT$"
	MSG3 DB 10,13,"CHARACTER IS NOT PRESENT$"
	PRINT DB "Insert letter $"
DATA ENDS
ASSUME CS:CODE, DS:DATA
CODE SEGMENT
	START:
	MOV AX,DATA
	MOV DS,AX
	LEA SI,MSG1
	LEA DX,PRINT
	MOV AH,9
	INT 21H
	MOV AH,1
	INT 21H
	MOV BL,'$'

NEXT:   CMP BL,[SI]
	JZ EXIT
	CMP AL,[SI]
	JZ PRES
	INC SI
	JMP NEXT

PRES:   LEA DX,MSG2
	MOV AH,9
	INT 21H
	MOV AH,4CH
	INT 21H

EXIT:   LEA DX,MSG3
	MOV AH,9
	INT 21H
	MOV AH,4CH
	INT 21H

CODE ENDS
END START