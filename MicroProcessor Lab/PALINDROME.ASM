DATA SEGMENT
STR1 db 5 dup(?)
REV1 db 5 dup(?)
MSG1 db 13,10,"Palindrome$"
MSG2 db 13,10,"Not Palindrome$"
MSG3 db 13,10,"Enter string $"
MSG4 db 13,10,"Reverse string $"
MSG5 db 13,10,"$"
PRINT MACRO MSG
	LEA DX,MSG
	MOV AH,09H
	INT 21H
ENDM
DATA ENDS
ASSUME CS:CODE,DS:DATA
CODE SEGMENT
START:
    MOV AX,DATA 
    MOV DS,AX
	MOV SI,00
	MOV CL,00
	PRINT MSG3
	PRINT MSG5
XX:MOV AH,01H
	INT 21H
	CMP AL,0DH
	JE YY
	MOV STR1[SI],AL
	INC SI
	INC CL
	JMP XX
YY:PRINT MSG4
	DEC SI
	MOV BL,CL
	MOV DI,00H
XX2:MOV DL,STR1[SI]
	MOV REV1[DI],DL
	MOV AH,02H
	INT 21H
	INC DI
	DEC SI
	DEC BL
	JNZ XX2
MOV SI,00
MOV DI,00
XX3:MOV DL,STR1[SI]
	CMP REV1[DI],DL
	JNE YY2
	INC SI
	INC DI
	LOOP XX3
	PRINT MSG1
	JMP EXIT
YY2:PRINT MSG2
EXIT:MOV AH,4CH
    INT 21H
CODE ENDS
END START